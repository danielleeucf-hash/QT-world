function setTtsState(state) {
  playTts.classList.toggle("is-active", state === "playing");
  pauseTts.classList.toggle("is-active", state === "paused");
  ttsStatus.textContent = {
    idle: "읽기 대기",
    loading: "CLOVA Dubbing 준비 중",
    playing: "읽는 중",
    paused: "일시정지",
    fallback: "브라우저 TTS",
    unsupported: "TTS 미지원"
  }[state];
}

let ttsAudio = null;
let meditationContext = null;
let meditationNodes = [];
let meditationPlaying = false;
let readingQueue = [];
let readingIndex = 0;
let stopRequested = false;
let pauseRequested = false;

function stopReading() {
  stopRequested = true;
  pauseRequested = false;
  readingQueue = [];
  readingIndex = 0;

  if (ttsAudio) {
    ttsAudio.pause();
    ttsAudio.currentTime = 0;
    ttsAudio = null;
  }

  if ("speechSynthesis" in window) {
    speechSynthesis.cancel();
  }

  setTtsState("idle");
}

function getBrowserVoice() {
  if (!("speechSynthesis" in window)) {
    return null;
  }

  return speechSynthesis.getVoices().find((voice) => voice.lang === "ko-KR")
    || speechSynthesis.getVoices().find((voice) => voice.lang.toLowerCase().startsWith("ko"))
    || null;
}

function wait(ms) {
  return new Promise((resolve) => {
    window.setTimeout(resolve, ms);
  });
}

function readWithBrowserTts(text, isLastSegment = true) {
  if (!("speechSynthesis" in window)) {
    setTtsState("unsupported");
    return Promise.resolve();
  }

  speechSynthesis.cancel();
  const utterance = new SpeechSynthesisUtterance(text);
  const voice = getBrowserVoice();

  utterance.lang = "ko-KR";
  utterance.rate = 0.78;
  utterance.pitch = 0.96;
  utterance.volume = 0.9;

  if (voice) {
    utterance.voice = voice;
  }

  return new Promise((resolve) => {
    utterance.onstart = () => setTtsState("fallback");
    utterance.onend = () => {
      if (isLastSegment) {
        setTtsState("idle");
      }
      resolve();
    };
    utterance.onerror = () => {
      if (isLastSegment) {
        setTtsState("idle");
      }
      resolve();
    };

    speechSynthesis.speak(utterance);
  });
}

async function readWithClovaDubbing(text, isLastSegment = true) {
  const endpoint = window.QT_CLOVA_DUBBING_ENDPOINT || "";
  const apiKey = window.QT_CLOVA_DUBBING_KEY || "";

  if (!endpoint || !apiKey) {
    await readWithBrowserTts(text, isLastSegment);
    return;
  }

  setTtsState("loading");

  const response = await fetch(endpoint, {
    method: "POST",
    headers: {
      "Content-Type": "application/json",
      "Authorization": `Bearer ${apiKey}`
    },
    body: JSON.stringify({
      text,
      voice: clovaDubbingVoice,
      format: "mp3"
    })
  });

  if (!response.ok) {
    throw new Error("CLOVA Dubbing request failed");
  }

  const audioBlob = await response.blob();
  ttsAudio = new Audio(URL.createObjectURL(audioBlob));
  ttsAudio.onplay = () => setTtsState("playing");
  ttsAudio.onended = () => {
    if (isLastSegment) {
      setTtsState("idle");
    }
  };
  ttsAudio.onerror = () => setTtsState("idle");
  await new Promise((resolve, reject) => {
    ttsAudio.onended = () => {
      if (isLastSegment) {
        setTtsState("idle");
      }
      resolve();
    };
    ttsAudio.onerror = reject;
    ttsAudio.play().catch(reject);
  });
}

async function playReadingQueue() {
  stopRequested = false;
  pauseRequested = false;

  while (readingIndex < readingQueue.length && !stopRequested) {
    if (pauseRequested) {
      return;
    }

    const segment = readingQueue[readingIndex];
    const isLastSegment = readingIndex === readingQueue.length - 1;

    try {
      await readWithClovaDubbing(segment, isLastSegment);
    } catch {
      await readWithBrowserTts(segment, isLastSegment);
    }

    readingIndex += 1;

    if (!isLastSegment && !stopRequested && !pauseRequested) {
      setTtsState("loading");
      await wait(520);
    }
  }

  if (!stopRequested && readingIndex >= readingQueue.length) {
    readingQueue = [];
    readingIndex = 0;
    setTtsState("idle");
  }
}

async function readSelectedPassage() {
  if (ttsAudio && ttsAudio.paused && ttsAudio.currentTime > 0) {
    pauseRequested = false;
    await ttsAudio.play();
    return;
  }

  if ("speechSynthesis" in window && speechSynthesis.paused) {
    pauseRequested = false;
    speechSynthesis.resume();
    setTtsState("fallback");
    return;
  }

  if (pauseRequested && readingQueue.length > 0) {
    await playReadingQueue();
    return;
  }

  const selected = getActiveDevotion();
  readingQueue = getReadableScripture(selected);
  readingIndex = 0;
  await playReadingQueue();
}

function pauseReading() {
  if (ttsAudio && !ttsAudio.paused) {
    ttsAudio.pause();
    pauseRequested = true;
    setTtsState("paused");
    return;
  }

  if (!("speechSynthesis" in window)) {
    setTtsState("unsupported");
    return;
  }

  if (speechSynthesis.speaking && !speechSynthesis.paused) {
    speechSynthesis.pause();
    pauseRequested = true;
    setTtsState("paused");
  }
}

function stopMeditationMusic() {
  if (!meditationPlaying) {
    return;
  }

  meditationNodes.forEach((node) => {
    if (node.gain) {
      node.gain.gain.setTargetAtTime(0, meditationContext.currentTime, 0.4);
    }
    if (node.oscillator) {
      window.setTimeout(() => node.oscillator.stop(), 700);
    }
  });

  meditationNodes = [];
  meditationPlaying = false;
  playMeditation.classList.remove("is-active");
  playMeditation.textContent = "명상음악 듣기";
}

function startMeditationMusic() {
  const AudioContext = window.AudioContext || window.webkitAudioContext;

  if (!AudioContext) {
    return;
  }

  meditationContext = meditationContext || new AudioContext();
  const frequencies = [196, 246.94, 329.63];
  const masterGain = meditationContext.createGain();
  masterGain.gain.setValueAtTime(0.035, meditationContext.currentTime);
  masterGain.connect(meditationContext.destination);

  meditationNodes = frequencies.map((frequency, index) => {
    const oscillator = meditationContext.createOscillator();
    const gain = meditationContext.createGain();
    const filter = meditationContext.createBiquadFilter();

    oscillator.type = "sine";
    oscillator.frequency.setValueAtTime(frequency, meditationContext.currentTime);
    oscillator.detune.setValueAtTime(index * 4, meditationContext.currentTime);
    filter.type = "lowpass";
    filter.frequency.setValueAtTime(900, meditationContext.currentTime);
    gain.gain.setValueAtTime(0, meditationContext.currentTime);
    gain.gain.linearRampToValueAtTime(0.16 / (index + 1), meditationContext.currentTime + 1.8);

    oscillator.connect(filter);
    filter.connect(gain);
    gain.connect(masterGain);
    oscillator.start();

    return { oscillator, gain };
  });

  meditationPlaying = true;
  playMeditation.classList.add("is-active");
  playMeditation.textContent = "명상음악 끄기";
}

function toggleMeditationMusic() {
  if (meditationPlaying) {
    stopMeditationMusic();
    return;
  }

  startMeditationMusic();
}

